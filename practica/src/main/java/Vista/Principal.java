    void Eliminar() {
        int fila = Tabla_Datos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Selecciona un usuario para eliminar.");
        } else {
            // Verificar que la celda que contiene el ID no sea null
            Object value = Tabla_Datos.getValueAt(fila, 0);
            if (value == null) {
                JOptionPane.showMessageDialog(null, "El valor de ID es nulo.");
                return;
            }

            // Convertir el valor de la celda en un entero (ID del usuario)
            String idStr = String.valueOf(value);  // Convertir a String
            if (idStr.isEmpty()) {
                JOptionPane.showMessageDialog(null, "El valor de ID es nulo.");
                return;
            }

            try {
                int id = Integer.parseInt(idStr);  // Convertir a int
                String sql = "DELETE FROM Clientes WHERE id = ?";
                try (Connection cn = con.getConnection(); PreparedStatement pst = cn.prepareStatement(sql)) {
                    pst.setInt(1, id);
                    int n = pst.executeUpdate();

                    if (n > 0) {
                        JOptionPane.showMessageDialog(null, "Usuario eliminado");
                        limpiar();
                        listar();
                    } else {
                        JOptionPane.showMessageDialog(null, "No se encontró el usuario con el ID proporcionado.");
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error al eliminar: " + e.getMessage());
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "El ID no es válido.");
            }
        }
    }

    void listar() {
        String sql = "SELECT * FROM Clientes"; // Cambié la tabla a "Clientes"

        try (Connection cn = con.getConnection(); Statement st = cn.createStatement(); ResultSet rs = st.executeQuery(sql)) {

            if (!rs.next()) {
                System.out.println("No se encontraron datos.");
                return;
            }

            System.out.println("Datos encontrados.");
            Object[] cliente = new Object[7];
            modelo = (DefaultTableModel) Tabla_Datos.getModel();
            modelo.setRowCount(0);  // Limpiar antes de cargar los datos

            do {
                cliente[0] = rs.getString("id");
                cliente[1] = rs.getString("dni");
                cliente[2] = rs.getString("nombres");
                cliente[3] = rs.getString("apellidos");
                cliente[4] = rs.getString("direccion");
                cliente[5] = rs.getString("celular");
                cliente[6] = rs.getString("sexo");
                modelo.addRow(cliente);
            } while (rs.next());

            Tabla_Datos.setModel(modelo);

        } catch (SQLException e) {
            System.out.println("Error al listar datos: " + e.getMessage());
            e.printStackTrace();
        }
    }

    void limpiar() {
        while (Tabla_Datos.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }


