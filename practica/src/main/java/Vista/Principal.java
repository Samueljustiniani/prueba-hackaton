    void agregar() {
        String nombres = jTextField2.getText();//nombres
        String apellidos = jTextField3.getText();//apellidos
        String direccion = jTextArea1.getText();//Direccion
        String dni = jTextField4.getText();//dni
        String telefono = jTextField5.getText();//CELULAR
        String generoSeleccionado = "";
        if (jRadioButton1.isSelected()) {
            generoSeleccionado = "Masculino";
        } else if (jRadioButton2.isSelected()) {
            generoSeleccionado = "Femenino";
        }

        String sql = "INSERT INTO Clientes (dni, nombres, apellidos, direccion, celular, sexo) VALUES (?, ?, ?, ?, ?, ?)";

        try {
            cn = con.getConnection();
            PreparedStatement pst = cn.prepareStatement(sql);
            // Asignación de valores a la consulta
            pst.setString(1, dni);
            pst.setString(2, nombres);
            pst.setString(3, apellidos);
            pst.setString(4, direccion);
            pst.setString(5, telefono);
            pst.setString(6, generoSeleccionado);

            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Registro agregado exitosamente");

                Principal listado = new Principal();
                listado.setVisible(true);
                this.dispose();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error en: " + e.getMessage());
        }
    }

